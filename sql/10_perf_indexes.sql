USE cinemadb;

-- sessions.start_time фильтруется в запросах 1, 3, 4 (WHERE DATE(start_time)
-- = CURDATE() / WHERE start_time >= ...). На 10M строках план подтвердил
-- полный скан 700 000 строк sessions ради 96 совпадений (запрос 1) и такой
-- же полный скан внутри JOIN (запрос 3); запрос 4 из-за этого же выбрал
-- обходной путь через movies (3000 циклов), заняв 6.3 секунды.
-- Единственный существующий индекс с start_time — uq_hall_start_time
-- (hall_id, start_time) — бесполезен без равенства по hall_id.
CREATE INDEX idx_sessions_start_time ON sessions (start_time);

-- bookings фильтруется в запросе 2 по status (равенство) и booking_date
-- (диапазон) одновременно — индекса на эту пару не было вовсе, план
-- показал полный скан 3 000 000 строк ради 5 340 совпадений.
-- Равенство — ведущей колонкой (стандартное правило для составных
-- индексов "равенство + диапазон").
CREATE INDEX idx_bookings_status_date ON bookings (status, booking_date);
